#pragma once
// =============================================================================
// kana_map.h  —  フリック入力 ローマ字マッピングテーブル (2ページ)
// =============================================================================
// PAGE 1 (通常): あかさたなはまやら
// PAGE 2 (ATOM S3ボタンで切替): わが ざだばぱ ん小文字記号
//
// グリッド配置（MPR121 電極番号と対応）:
//   電極 0: [P1]あ行  / [P2]わ行
//   電極 1: [P1]か行  / [P2]が行
//   電極 2: [P1]さ行  / [P2]ざ行
//   電極 3: [P1]た行  / [P2]だ行
//   電極 4: [P1]な行  / [P2]ば行
//   電極 5: [P1]は行  / [P2]ぱ行
//   電極 6: [P1]ま行  / [P2]ん
//   電極 7: [P1]や行  / [P2]小文字 (l+母音)
//   電極 8: [P1]ら行  / [P2]記号
//
// MCP23017 GPA:
//   GPA0=↑  GPA1=←  GPA2=↓  GPA3=→  (フリック方向 active-LOW)
//   GPA4=BS (バックスペース)
//   GPA5=SP (スペース / 変換候補送り)
//
// フリック方向インデックス:
//   0=中央(タップ)  1=上  2=左  3=下  4=右
// =============================================================================

#include <Arduino.h>

// ── ページ数 ─────────────────────────────────────────────────────────────────
static constexpr uint8_t PAGE_COUNT = 2;

// ── PAGE 1: あかさたなはまやら ─────────────────────────────────────────────────
static const char* const KANA_MAP[9][5] = {
    //  中央   上     左     下     右
    { "a",  "i",  "u",  "e",  "o"  },  // [0] あ行: あいうえお
    { "ka", "ki", "ku", "ke", "ko" },  // [1] か行: かきくけこ
    { "sa", "si", "su", "se", "so" },  // [2] さ行: さしすせそ (しはsi/shi両対応)
    { "ta", "ti", "tu", "te", "to" },  // [3] た行: たちつてと
    { "na", "ni", "nu", "ne", "no" },  // [4] な行: なにぬねの
    { "ha", "hi", "fu", "he", "ho" },  // [5] は行: はひふへほ
    { "ma", "mi", "mu", "me", "mo" },  // [6] ま行: まみむめも
    { "ya", "i",  "yu", "e",  "yo" },  // [7] や行: やいゆえよ
    { "ra", "ri", "ru", "re", "ro" },  // [8] ら行: らりるれろ
};

static const char* const PAD_LABEL[9] = {
    "あ", "か", "さ",
    "た", "な", "は",
    "ま", "や", "ら",
};

// ── PAGE 2: わが行/ざ/だ/ば/ぱ/ん/小/記号 ──────────────────────────────────────
// 記号パッド: . → 。  - → ー  , → 、  ! → ！  ? → ？
static const char* const KANA_MAP2[9][5] = {
    //  中央   上       左      下      右
    { "wa",  "wi",    "wu",   "we",   "wo"  },  // [0] わ行: わゐ(い)ゑ(え)を
    { "ga",  "gi",    "gu",   "ge",   "go"  },  // [1] が行: がぎぐげご
    { "za",  "zi",    "zu",   "ze",   "zo"  },  // [2] ざ行: ざじずぜぞ
    { "da",  "di",    "du",   "de",   "do"  },  // [3] だ行: だぢづでど
    { "ba",  "bi",    "bu",   "be",   "bo"  },  // [4] ば行: ばびぶべぼ
    { "pa",  "pi",    "pu",   "pe",   "po"  },  // [5] ぱ行: ぱぴぷぺぽ
    { "nn",  "n",     "nn",   "nn",   "nn"  },  // [6] ん: 全方向 nn
    { "ltu", "la",    "lu",   "lo",   "li"  },  // [7] 小文字: っぁぅぉぃ
    { ".",   "-",     ",",    "!",    "?"   },  // [8] 記号: 。ー、！？
};

static const char* const PAD_LABEL2[9] = {
    "わ", "が", "ざ",
    "だ", "ば", "ぱ",
    "ん", "小", "記",
};

// ── 共通アクセサ ──────────────────────────────────────────────────────────────
inline const char* kanaGet(uint8_t page, uint8_t pad, uint8_t dir) {
    if (page == 0) return KANA_MAP[pad][dir];
    return KANA_MAP2[pad][dir];
}

inline const char* padLabel(uint8_t page, uint8_t pad) {
    if (page == 0) return PAD_LABEL[pad];
    return PAD_LABEL2[pad];
}

// ── ラベル ────────────────────────────────────────────────────────────────────
static const char* const DIR_LABEL[5] = { "中", "上", "左", "下", "右" };

// ── フリック方向 enum ─────────────────────────────────────────────────────────
enum FlickDir : uint8_t {
    DIR_CENTER = 0,
    DIR_UP     = 1,
    DIR_LEFT   = 2,
    DIR_DOWN   = 3,
    DIR_RIGHT  = 4,
};
